<?php

function echo_array($array) {
	echo '<pre class="pre-array">';
	print_r($array);
	echo '</pre>';
}

function echo_info($array) {
	echo '<pre class="pre-info">';
	print_r($array);
	echo '</pre>';
}

function echo_error($array) {
	echo '<pre class="pre-error">';
	print_r($array);
	echo '</pre>';
}

?>